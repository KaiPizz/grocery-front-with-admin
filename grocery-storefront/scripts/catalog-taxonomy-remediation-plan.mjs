#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const DEFAULT_INPUT = 'docs/asiandeligo-catalog-taxonomy-decisions-20260713.json';
const DEFAULT_OUTPUT = 'docs/asiandeligo-catalog-taxonomy-remediation-20260713.md';
const DEFAULT_APPLY = 'docs/asiandeligo-catalog-taxonomy-remediation-apply-20260713.sql';
const DEFAULT_ROLLBACK = 'docs/asiandeligo-catalog-taxonomy-remediation-rollback-20260713.sql';

function parseArgs() {
  const options = {
    input: DEFAULT_INPUT,
    output: DEFAULT_OUTPUT,
    apply: DEFAULT_APPLY,
    rollback: DEFAULT_ROLLBACK,
  };

  const args = process.argv.slice(2);
  for (let index = 0; index < args.length; index += 2) {
    const flag = args[index];
    const value = args[index + 1];
    if (!['--input', '--output', '--apply', '--rollback'].includes(flag) || !value) {
      throw new Error(`Invalid argument near ${flag ?? '(end)'}`);
    }
    options[flag.slice(2)] = value;
  }
  return options;
}

function sqlLiteral(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlBoolean(value) {
  return value ? 'TRUE' : 'FALSE';
}

function assertUnique(items, key, label) {
  const seen = new Set();
  for (const item of items) {
    const value = item[key];
    if (!value || seen.has(value)) {
      throw new Error(`${label} must contain unique non-empty ${key} values`);
    }
    seen.add(value);
  }
}

function validate(decisions) {
  if (decisions.version !== 1 || decisions.channel !== 'asiandeligo') {
    throw new Error('Unsupported decision file or channel');
  }
  if (decisions.categoryMoves?.length !== 18) {
    throw new Error('Expected exactly 18 category moves');
  }
  if (decisions.categoryStateChanges?.length !== 4) {
    throw new Error('Expected exactly 4 category state changes');
  }

  assertUnique(decisions.categoryMoves, 'sku', 'categoryMoves');
  assertUnique(decisions.categoryStateChanges, 'slug', 'categoryStateChanges');

  for (const row of decisions.categoryMoves) {
    if (!row.name || !row.reason || !row.expectedCategorySlug || !row.targetCategorySlug) {
      throw new Error(`Incomplete category move for ${row.sku}`);
    }
    if (row.expectedCategorySlug === row.targetCategorySlug) {
      throw new Error(`Category move does not change category for ${row.sku}`);
    }
  }

  for (const row of decisions.categoryStateChanges) {
    if (row.expectedActive !== true || row.targetActive !== false) {
      throw new Error(`Unexpected category state transition for ${row.slug}`);
    }
    if (!Number.isInteger(row.expectedProductsBeforeMove) || row.expectedProductsBeforeMove < 1) {
      throw new Error(`Invalid product precondition for ${row.slug}`);
    }
  }
}

function renderMoveValues(decisions, direction) {
  const isApply = direction === 'apply';
  const stateBySlug = new Map(decisions.categoryStateChanges.map((row) => [row.slug, row]));

  return decisions.categoryMoves.map((row) => {
    const fromSlug = isApply ? row.expectedCategorySlug : row.targetCategorySlug;
    const toSlug = isApply ? row.targetCategorySlug : row.expectedCategorySlug;
    const targetExpectedActive = isApply ? true : (stateBySlug.get(toSlug)?.targetActive ?? true);
    return `  (${sqlLiteral(row.sku)}, ${sqlLiteral(fromSlug)}, ${sqlLiteral(toSlug)}, ${sqlBoolean(targetExpectedActive)})`;
  }).join(',\n');
}

function renderCategoryStateValues(decisions, direction) {
  const isApply = direction === 'apply';
  return decisions.categoryStateChanges.map((row) => {
    const expectedActive = isApply ? row.expectedActive : row.targetActive;
    const targetActive = isApply ? row.targetActive : row.expectedActive;
    const expectedProducts = isApply ? row.expectedProductsBeforeMove : 0;
    return `  (${sqlLiteral(row.slug)}, ${sqlBoolean(expectedActive)}, ${sqlBoolean(targetActive)}, ${expectedProducts})`;
  }).join(',\n');
}

function renderSql(decisions, direction) {
  const isApply = direction === 'apply';
  const action = isApply ? 'apply' : 'rollback';

  return `-- Generated by scripts/catalog-taxonomy-remediation-plan.mjs
-- Purpose: ${action} the guarded Asia Deli Go taxonomy consolidation.
-- No product price, stock, content, publication, order, or reservation field is changed.
-- Source categories are deactivated, never deleted.
--
-- JOIN contract (schema-introspect verified 2026-07-13):
--   channels.salon_id (uuid) scopes categories, products, and product_variants.
--   products.category_id (uuid) = categories.id (uuid), FK declared.
--   product_variants.template_id (uuid) = products.id (uuid), FK declared.
--   Canonical tables are categories/products/product_variants; backup tables are excluded.

\\set ON_ERROR_STOP on
BEGIN;

CREATE TEMP TABLE _adg_taxonomy_target_salon (salon_id uuid PRIMARY KEY) ON COMMIT DROP;
INSERT INTO _adg_taxonomy_target_salon (salon_id)
SELECT salon_id FROM channels WHERE slug = ${sqlLiteral(decisions.channel)} AND is_active = TRUE;

CREATE TEMP TABLE _adg_taxonomy_moves (
  sku text PRIMARY KEY,
  expected_category_slug text NOT NULL,
  target_category_slug text NOT NULL,
  target_category_expected_active boolean NOT NULL
) ON COMMIT DROP;
INSERT INTO _adg_taxonomy_moves (
  sku, expected_category_slug, target_category_slug, target_category_expected_active
) VALUES
${renderMoveValues(decisions, direction)};

CREATE TEMP TABLE _adg_taxonomy_category_state (
  slug text PRIMARY KEY,
  expected_active boolean NOT NULL,
  target_active boolean NOT NULL,
  expected_products integer NOT NULL
) ON COMMIT DROP;
INSERT INTO _adg_taxonomy_category_state (
  slug, expected_active, target_active, expected_products
) VALUES
${renderCategoryStateValues(decisions, direction)};

DO $$
DECLARE
  affected integer;
BEGIN
  IF (SELECT COUNT(*) FROM _adg_taxonomy_target_salon) <> 1 THEN
    RAISE EXCEPTION 'Expected exactly one active Asia Deli Go channel';
  END IF;

  IF EXISTS (
    SELECT state.slug
    FROM _adg_taxonomy_category_state state
    CROSS JOIN _adg_taxonomy_target_salon tenant
    LEFT JOIN categories category
      ON category.salon_id = tenant.salon_id
     AND category.slug = state.slug
    LEFT JOIN products product
      ON product.salon_id = tenant.salon_id
     AND product.category_id = category.id
     AND product.deleted_at IS NULL
    GROUP BY state.slug, state.expected_active, state.expected_products
    HAVING COUNT(DISTINCT category.id) <> 1
       OR BOOL_OR(category.is_active) IS DISTINCT FROM state.expected_active
       OR COUNT(DISTINCT product.id) <> state.expected_products
  ) THEN
    RAISE EXCEPTION 'Source category state precondition failed; aborting all changes';
  END IF;

  IF EXISTS (
    SELECT move.sku
    FROM _adg_taxonomy_moves move
    CROSS JOIN _adg_taxonomy_target_salon tenant
    LEFT JOIN product_variants variant
      ON variant.salon_id = tenant.salon_id
     AND variant.sku = move.sku
     AND variant.is_active = TRUE
     AND variant.deleted_at IS NULL
    LEFT JOIN products product
      ON product.salon_id = tenant.salon_id
     AND product.id = variant.template_id
     AND product.is_active = TRUE
     AND product.is_published = TRUE
     AND product.deleted_at IS NULL
    LEFT JOIN categories current_category
      ON current_category.salon_id = tenant.salon_id
     AND current_category.id = product.category_id
    LEFT JOIN categories target_category
      ON target_category.salon_id = tenant.salon_id
     AND target_category.slug = move.target_category_slug
    GROUP BY move.sku, move.expected_category_slug, move.target_category_expected_active
    HAVING COUNT(DISTINCT variant.id) <> 1
       OR COUNT(DISTINCT product.id) <> 1
       OR COUNT(DISTINCT current_category.id) <> 1
       OR MIN(current_category.slug) IS DISTINCT FROM move.expected_category_slug
       OR BOOL_OR(current_category.is_active) IS DISTINCT FROM TRUE
       OR COUNT(DISTINCT target_category.id) <> 1
       OR BOOL_OR(target_category.is_active) IS DISTINCT FROM move.target_category_expected_active
  ) THEN
    RAISE EXCEPTION 'Product/category mapping precondition failed; aborting all changes';
  END IF;

  ${isApply ? '' : `UPDATE categories category
  SET is_active = state.target_active,
      updated_at = NOW()
  FROM _adg_taxonomy_target_salon tenant,
       _adg_taxonomy_category_state state
  WHERE category.salon_id = tenant.salon_id
    AND category.slug = state.slug
    AND category.is_active = state.expected_active;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.categoryStateChanges.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.categoryStateChanges.length} category reactivations, got %', affected;
  END IF;`}

  UPDATE products product
  SET category_id = target_category.id,
      updated_at = NOW()
  FROM _adg_taxonomy_target_salon tenant,
       _adg_taxonomy_moves move,
       product_variants variant,
       categories current_category,
       categories target_category
  WHERE product.salon_id = tenant.salon_id
    AND variant.salon_id = tenant.salon_id
    AND variant.template_id = product.id
    AND variant.sku = move.sku
    AND variant.is_active = TRUE
    AND variant.deleted_at IS NULL
    AND current_category.salon_id = tenant.salon_id
    AND current_category.id = product.category_id
    AND current_category.slug = move.expected_category_slug
    AND target_category.salon_id = tenant.salon_id
    AND target_category.slug = move.target_category_slug
    AND product.is_active = TRUE
    AND product.is_published = TRUE
    AND product.deleted_at IS NULL;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.categoryMoves.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.categoryMoves.length} product category updates, got %', affected;
  END IF;

  ${isApply ? `IF EXISTS (
    SELECT state.slug
    FROM _adg_taxonomy_category_state state
    CROSS JOIN _adg_taxonomy_target_salon tenant
    JOIN categories category
      ON category.salon_id = tenant.salon_id
     AND category.slug = state.slug
    JOIN products product
      ON product.salon_id = tenant.salon_id
     AND product.category_id = category.id
     AND product.deleted_at IS NULL
  ) THEN
    RAISE EXCEPTION 'A source category still owns products after remapping';
  END IF;

  UPDATE categories category
  SET is_active = state.target_active,
      updated_at = NOW()
  FROM _adg_taxonomy_target_salon tenant,
       _adg_taxonomy_category_state state
  WHERE category.salon_id = tenant.salon_id
    AND category.slug = state.slug
    AND category.is_active = state.expected_active;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.categoryStateChanges.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.categoryStateChanges.length} category deactivations, got %', affected;
  END IF;` : ''}
END $$;

SELECT variant.sku, product.name, category.name AS category_name, category.slug AS category_slug
FROM _adg_taxonomy_target_salon tenant
JOIN product_variants variant ON variant.salon_id = tenant.salon_id
JOIN _adg_taxonomy_moves move ON move.sku = variant.sku
JOIN products product ON product.salon_id = tenant.salon_id AND product.id = variant.template_id
JOIN categories category ON category.salon_id = tenant.salon_id AND category.id = product.category_id
ORDER BY variant.sku;

SELECT category.slug, category.is_active
FROM _adg_taxonomy_target_salon tenant
JOIN categories category ON category.salon_id = tenant.salon_id
JOIN _adg_taxonomy_category_state state ON state.slug = category.slug
ORDER BY category.slug;

COMMIT;
`.replace(/[ \t]+\n/g, '\n');
}

function renderMarkdown(decisions, options) {
  const lines = [
    '# Asia Deli Go Catalog Taxonomy Remediation',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    '## Result',
    '',
    `- Product category moves prepared: ${decisions.categoryMoves.length}.`,
    `- Empty source categories to deactivate: ${decisions.categoryStateChanges.length}.`,
    '- Source categories are retained for rollback; none are deleted.',
    '- Production mutation in this step: none.',
    '',
    '## Moves',
    '',
    '| SKU | Product | From | To | Reason |',
    '| --- | --- | --- | --- | --- |',
    ...decisions.categoryMoves.map((row) => `| ${row.sku} | ${row.name} | ${row.expectedCategorySlug} | ${row.targetCategorySlug} | ${row.reason} |`),
    '',
    '## Category state',
    '',
    '| Category | Current live/not-deleted rows | Apply state |',
    '| --- | ---: | --- |',
    ...decisions.categoryStateChanges.map((row) => `| ${row.slug} | ${row.expectedProductsBeforeMove} | active -> inactive |`),
    '',
    '## Guardrails',
    '',
    '- Every operation is scoped through the single active `asiandeligo` channel salon.',
    '- Apply requires each SKU to resolve to one active/published product and the exact expected source category.',
    '- Apply requires all target categories to exist and be active.',
    '- A source category is deactivated only after it owns zero non-deleted products.',
    '- Rollback requires the exact post-apply state and reactivates source categories before moving products back.',
    '- No price, stock, content, publication, order, or reservation field is changed.',
    '',
    '## Notes',
    '',
    ...decisions.notes.map((note) => `- ${note}`),
    '',
    '## Artifacts',
    '',
    `- Decisions: \`${options.input}\``,
    `- Guarded apply: \`${options.apply}\``,
    `- Guarded rollback: \`${options.rollback}\``,
  ];
  return `${lines.join('\n')}\n`;
}

function writeFile(filePath, contents) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, contents);
}

function main() {
  const options = parseArgs();
  const decisions = JSON.parse(fs.readFileSync(options.input, 'utf8'));
  validate(decisions);
  writeFile(options.apply, renderSql(decisions, 'apply'));
  writeFile(options.rollback, renderSql(decisions, 'rollback'));
  writeFile(options.output, renderMarkdown(decisions, options));
  console.log(`Validated ${decisions.categoryMoves.length} product moves and ${decisions.categoryStateChanges.length} category state changes.`);
  console.log(`Wrote ${options.output}`);
  console.log(`Wrote ${options.apply}`);
  console.log(`Wrote ${options.rollback}`);
}

main();
