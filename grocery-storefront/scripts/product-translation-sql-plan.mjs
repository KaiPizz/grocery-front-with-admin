#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const DEFAULT_INPUT = 'docs/product-translation-en-dry-run.json';
const DEFAULT_OUTPUT = 'docs/product-translation-en-apply.sql';
const DEFAULT_REPORT = 'docs/product-translation-en-sql-plan.md';
const DEFAULT_CHANNEL = 'kenmito';
const DEFAULT_LANGUAGE = 'en';

function printUsage(exitCode = 0, errorMessage = null) {
  if (errorMessage) {
    console.error(`Error: ${errorMessage}`);
    console.error('');
  }

  console.log('Usage: node scripts/product-translation-sql-plan.mjs [options]');
  console.log('');
  console.log('Generates reviewed SQL to upsert product_translations for one storefront channel.');
  console.log('It does not connect to the database or mutate data.');
  console.log('');
  console.log('Options:');
  console.log(`  --input <path>      Input dry-run JSON (default: ${DEFAULT_INPUT})`);
  console.log(`  --output <path>     SQL output path (default: ${DEFAULT_OUTPUT})`);
  console.log(`  --report <path>     Markdown report path (default: ${DEFAULT_REPORT})`);
  console.log(`  --channel <slug>    Channel slug (default: ${DEFAULT_CHANNEL})`);
  console.log(`  --language <code>   Target language code (default: ${DEFAULT_LANGUAGE})`);
  console.log('  --help              Show this help message');
  process.exit(exitCode);
}

function parseArgs() {
  const options = {
    input: DEFAULT_INPUT,
    output: DEFAULT_OUTPUT,
    report: DEFAULT_REPORT,
    channel: DEFAULT_CHANNEL,
    language: DEFAULT_LANGUAGE,
  };

  const args = process.argv.slice(2);
  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    if (arg === '--help') printUsage(0);
    if (!arg.startsWith('--')) printUsage(1, `Unexpected argument "${arg}"`);

    const value = args[index + 1];
    if (value == null || value.startsWith('--')) {
      printUsage(1, `Missing value for "${arg}"`);
    }

    switch (arg.slice(2)) {
      case 'input':
        options.input = value;
        break;
      case 'output':
        options.output = value;
        break;
      case 'report':
        options.report = value;
        break;
      case 'channel':
        options.channel = value;
        break;
      case 'language':
        options.language = value;
        break;
      default:
        printUsage(1, `Unknown option "${arg}"`);
    }
    index += 1;
  }

  return options;
}

function readRows(inputPath) {
  const resolvedPath = path.resolve(process.cwd(), inputPath);
  if (!fs.existsSync(resolvedPath)) throw new Error(`Input file not found: ${inputPath}`);

  const data = JSON.parse(fs.readFileSync(resolvedPath, 'utf8'));
  if (!Array.isArray(data)) throw new Error(`Input JSON must be an array: ${inputPath}`);
  return data;
}

function cleanText(value) {
  const text = String(value ?? '')
    .replace(/\r/g, '\n')
    .replace(/[ \t]+/g, ' ')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
  return text || null;
}

function validateRows(rows) {
  const errors = [];
  rows.forEach((row, index) => {
    const label = `row ${index + 1}${row?.slug ? ` (${row.slug})` : ''}`;
    if (!row || typeof row !== 'object') errors.push(`${label}: invalid row`);
    if (!row.id || typeof row.id !== 'string') errors.push(`${label}: missing product id`);
    if (row.status !== 'translated') errors.push(`${label}: status is "${row.status}"`);
    if (row.notes) errors.push(`${label}: notes is not empty: ${row.notes}`);
    if (!cleanText(row.proposedEnglishName)) errors.push(`${label}: missing proposedEnglishName`);
    if (!cleanText(row.proposedEnglishDescription)) {
      errors.push(`${label}: missing proposedEnglishDescription`);
    }
  });
  return errors;
}

function sqlString(value) {
  if (value == null || value === '') return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function buildValues(rows) {
  return rows.map((row) => {
    const name = cleanText(row.proposedEnglishName);
    const description = cleanText(row.proposedEnglishDescription);
    return [
      sqlString(row.id),
      sqlString(row.slug),
      sqlString(name),
      sqlString(description),
      sqlString(name),
      sqlString(description),
    ].join(', ');
  });
}

function writeSql(options, rows) {
  const backupTable = `kenmito_product_translations_backup_${new Date().toISOString().slice(0, 10).replace(/-/g, '')}`;
  const values = buildValues(rows);

  const sql = [
    '-- Generated by scripts/product-translation-sql-plan.mjs',
    '-- Safe behavior: Kenmito channel only; upserts one language; backs up existing rows first.',
    'BEGIN;',
    '',
    `CREATE TABLE IF NOT EXISTS ${backupTable} (LIKE product_translations INCLUDING ALL);`,
    '',
    'WITH source(template_id, slug, product_name, short_description, meta_title, meta_description) AS (',
    `  VALUES\n  (${values.join('),\n  (')})`,
    '), matched AS (',
    '  SELECT',
    '    p.salon_id,',
    '    p.id AS template_id,',
    '    s.slug,',
    '    s.product_name,',
    '    s.short_description,',
    '    s.meta_title,',
    '    s.meta_description',
    '  FROM source s',
    `  JOIN channels ch ON ch.slug = ${sqlString(options.channel)} AND ch.is_active = true`,
    '  JOIN products p ON p.id = s.template_id::uuid AND p.salon_id = ch.salon_id',
    '  WHERE p.deleted_at IS NULL',
    '), backup_insert AS (',
    `  INSERT INTO ${backupTable}`,
    '  SELECT pt.*',
    '  FROM product_translations pt',
    '  JOIN matched m ON m.template_id = pt.template_id AND m.salon_id = pt.salon_id',
    `  WHERE pt.lang_id = ${sqlString(options.language)}`,
    `    AND NOT EXISTS (SELECT 1 FROM ${backupTable} b WHERE b.id = pt.id)`,
    '  RETURNING id',
    '), upserted AS (',
    '  INSERT INTO product_translations (',
    '    id, salon_id, template_id, lang_id, product_name, short_description,',
    '    meta_title, meta_description, created_at, updated_at',
    '  )',
    '  SELECT',
    '    gen_random_uuid(),',
    '    salon_id,',
    '    template_id,',
    `    ${sqlString(options.language)},`,
    '    product_name,',
    '    short_description,',
    '    meta_title,',
    '    meta_description,',
    '    NOW(),',
    '    NOW()',
    '  FROM matched',
    '  ON CONFLICT (template_id, lang_id) DO UPDATE SET',
    '    product_name = EXCLUDED.product_name,',
    '    short_description = EXCLUDED.short_description,',
    '    meta_title = EXCLUDED.meta_title,',
    '    meta_description = EXCLUDED.meta_description,',
    '    updated_at = NOW()',
    '  WHERE product_translations.salon_id = EXCLUDED.salon_id',
    '  RETURNING template_id',
    ')',
    'SELECT',
    "  (SELECT COUNT(*) FROM source) AS source_rows,",
    "  (SELECT COUNT(*) FROM matched) AS matched_rows,",
    "  (SELECT COUNT(*) FROM backup_insert) AS backed_up_rows,",
    "  (SELECT COUNT(*) FROM upserted) AS upserted_rows;",
    '',
    '-- Verify the printed counts after running. The backup table can be used to restore previous EN rows if needed.',
    'COMMIT;',
    '',
  ].join('\n');

  fs.mkdirSync(path.dirname(options.output), { recursive: true });
  fs.writeFileSync(options.output, sql);
}

function writeReport(options, rows, errors) {
  const lines = [
    '# Product English Translation SQL Plan',
    '',
    `Generated: ${new Date().toISOString()}`,
    `Channel: ${options.channel}`,
    `Language: ${options.language}`,
    `Input rows: ${rows.length}`,
    `Validation errors: ${errors.length}`,
    `SQL output: ${options.output}`,
    '',
  ];

  if (errors.length) {
    lines.push('## Validation Errors', '');
    errors.forEach((error) => lines.push(`- ${error}`));
    lines.push('');
  }

  lines.push('## Rows', '');
  lines.push('| Slug | Product ID | English name |');
  lines.push('| --- | --- | --- |');
  rows.forEach((row) => {
    lines.push(`| ${row.slug} | ${row.id} | ${cleanText(row.proposedEnglishName)?.replace(/\|/g, '\\|')} |`);
  });

  fs.mkdirSync(path.dirname(options.report), { recursive: true });
  fs.writeFileSync(options.report, `${lines.join('\n')}\n`);
}

function main() {
  const options = parseArgs();
  const rows = readRows(options.input);
  const errors = validateRows(rows);

  writeReport(options, rows, errors);
  if (errors.length) {
    throw new Error(`Validation failed with ${errors.length} error(s). See ${options.report}`);
  }

  writeSql(options, rows);
  console.log(`Rows planned: ${rows.length}`);
  console.log(`SQL: ${options.output}`);
  console.log(`Report: ${options.report}`);
}

try {
  main();
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
