#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const DEFAULT_INPUT = 'docs/asiandeligo-catalog-blocker-unpublish-decisions-20260713.json';
const DEFAULT_OUTPUT = 'docs/asiandeligo-catalog-blocker-unpublish-20260713.md';
const DEFAULT_APPLY = 'docs/asiandeligo-catalog-blocker-unpublish-apply-20260713.sql';
const DEFAULT_ROLLBACK = 'docs/asiandeligo-catalog-blocker-unpublish-rollback-20260713.sql';
const DEFAULT_REVIEW_CSV = 'docs/asiandeligo-catalog-blocker-owner-review-20260713.csv';

function parseArgs() {
  const options = {
    input: DEFAULT_INPUT,
    output: DEFAULT_OUTPUT,
    apply: DEFAULT_APPLY,
    rollback: DEFAULT_ROLLBACK,
    reviewCsv: DEFAULT_REVIEW_CSV,
  };

  const args = process.argv.slice(2);
  for (let index = 0; index < args.length; index += 2) {
    const flag = args[index];
    const value = args[index + 1];
    const keyByFlag = {
      '--input': 'input',
      '--output': 'output',
      '--apply': 'apply',
      '--rollback': 'rollback',
      '--review-csv': 'reviewCsv',
    };
    if (!keyByFlag[flag] || !value) {
      throw new Error(`Invalid argument near ${flag ?? '(end)'}`);
    }
    options[keyByFlag[flag]] = value;
  }

  return options;
}

function sqlLiteral(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function sqlBoolean(value) {
  return value ? 'TRUE' : 'FALSE';
}

function csvEscape(value) {
  const text = value == null ? '' : String(value);
  return /[",\n\r]/.test(text) ? `"${text.replaceAll('"', '""')}"` : text;
}

function validate(decisions) {
  if (decisions.version !== 1 || decisions.channel !== 'asiandeligo') {
    throw new Error('Unsupported decision file or channel');
  }
  if (decisions.products?.length !== 5) {
    throw new Error('Expected exactly 5 blocker products');
  }

  const skus = new Set();
  const slugs = new Set();
  for (const product of decisions.products) {
    if (!/^ADG-\d{6}$/.test(product.sku) || skus.has(product.sku)) {
      throw new Error(`Invalid or duplicate SKU: ${product.sku}`);
    }
    if (!product.slug || slugs.has(product.slug)) {
      throw new Error(`Missing or duplicate slug for ${product.sku}`);
    }
    if (!product.name || !product.observedIdentity || !product.requiredEvidence) {
      throw new Error(`Incomplete review evidence for ${product.sku}`);
    }
    if (
      product.expectedStatus !== 'active'
      || product.targetStatus !== 'draft'
      || product.expectedPublished !== true
      || product.targetPublished !== false
    ) {
      throw new Error(`Unexpected publication transition for ${product.sku}`);
    }
    skus.add(product.sku);
    slugs.add(product.slug);
  }
}

function renderTargetValues(decisions, direction) {
  const isApply = direction === 'apply';
  return decisions.products.map((product) => {
    const expectedStatus = isApply ? product.expectedStatus : product.targetStatus;
    const targetStatus = isApply ? product.targetStatus : product.expectedStatus;
    const expectedPublished = isApply ? product.expectedPublished : product.targetPublished;
    const targetPublished = isApply ? product.targetPublished : product.expectedPublished;
    return [
      '  (',
      sqlLiteral(product.sku),
      `, ${sqlLiteral(product.name)}`,
      `, ${sqlLiteral(product.slug)}`,
      `, ${sqlLiteral(expectedStatus)}`,
      `, ${sqlLiteral(targetStatus)}`,
      `, ${sqlBoolean(expectedPublished)}`,
      `, ${sqlBoolean(targetPublished)}`,
      ')',
    ].join('');
  }).join(',\n');
}

function renderSql(decisions, direction) {
  const isApply = direction === 'apply';
  const action = isApply ? 'temporarily unpublish' : 'rollback publication for';
  const transition = isApply ? 'active/published -> draft/unpublished' : 'draft/unpublished -> active/published';

  return `-- Generated by scripts/catalog-blocker-unpublish-plan.mjs
-- Purpose: ${action} five Asia Deli Go products with verified image/product identity mismatches.
-- Transition: ${transition}.
-- No SKU, slug, price, stock, image, content, category, variant, order, or reservation field is changed.
-- Reindex channel asiandeligo after commit so Meilisearch receives the publication-state change.
--
-- JOIN contract (schema-introspect verified on production 2026-07-13):
--   channels.salon_id (uuid) scopes products and product_variants.
--   product_variants.template_id (uuid) = products.id (uuid), FK declared and five target matches verified.
--   Every business-table join includes salon_id; products/variants exclude deleted_at rows.
--   Canonical tables only: channels/products/product_variants; backup tables are excluded.
--   products.status is varchar; live values active/draft were verified for the target salon.

\\set ON_ERROR_STOP on
BEGIN;

CREATE TEMP TABLE _adg_blocker_target_salon (salon_id uuid PRIMARY KEY) ON COMMIT DROP;
INSERT INTO _adg_blocker_target_salon (salon_id)
SELECT salon_id FROM channels WHERE slug = ${sqlLiteral(decisions.channel)} AND is_active = TRUE;

CREATE TEMP TABLE _adg_blocker_targets (
  sku text PRIMARY KEY,
  expected_name text NOT NULL,
  expected_slug text NOT NULL UNIQUE,
  expected_status text NOT NULL,
  target_status text NOT NULL,
  expected_published boolean NOT NULL,
  target_published boolean NOT NULL
) ON COMMIT DROP;
INSERT INTO _adg_blocker_targets (
  sku, expected_name, expected_slug, expected_status, target_status,
  expected_published, target_published
) VALUES
${renderTargetValues(decisions, direction)};

DO $$
DECLARE
  affected integer;
BEGIN
  IF (SELECT COUNT(*) FROM _adg_blocker_target_salon) <> 1 THEN
    RAISE EXCEPTION 'Expected exactly one active Asia Deli Go channel';
  END IF;

  IF EXISTS (
    SELECT target.sku
    FROM _adg_blocker_targets target
    CROSS JOIN _adg_blocker_target_salon tenant
    LEFT JOIN product_variants variant
      ON variant.salon_id = tenant.salon_id
     AND variant.sku = target.sku
     AND variant.is_active = TRUE
     AND variant.deleted_at IS NULL
    LEFT JOIN products product
      ON product.salon_id = tenant.salon_id
     AND product.id = variant.template_id
     AND product.is_active = TRUE
     AND product.deleted_at IS NULL
    GROUP BY target.sku, target.expected_name, target.expected_slug,
             target.expected_status, target.expected_published
    HAVING COUNT(DISTINCT variant.id) <> 1
       OR COUNT(DISTINCT product.id) <> 1
       OR MIN(product.name) IS DISTINCT FROM target.expected_name
       OR MIN(product.slug) IS DISTINCT FROM target.expected_slug
       OR MIN(product.status) IS DISTINCT FROM target.expected_status
       OR BOOL_OR(product.is_published) IS DISTINCT FROM target.expected_published
  ) THEN
    RAISE EXCEPTION 'Blocker publication precondition failed; aborting all changes';
  END IF;

  UPDATE products product
  SET status = target.target_status,
      is_published = target.target_published,
      updated_at = NOW()
  FROM _adg_blocker_target_salon tenant,
       _adg_blocker_targets target,
       product_variants variant
  WHERE product.salon_id = tenant.salon_id
    AND variant.salon_id = tenant.salon_id
    AND variant.template_id = product.id
    AND variant.sku = target.sku
    AND variant.is_active = TRUE
    AND variant.deleted_at IS NULL
    AND product.is_active = TRUE
    AND product.deleted_at IS NULL
    AND product.name = target.expected_name
    AND product.slug = target.expected_slug
    AND product.status = target.expected_status
    AND product.is_published = target.expected_published;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.products.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.products.length} publication updates, got %', affected;
  END IF;

  IF EXISTS (
    SELECT target.sku
    FROM _adg_blocker_targets target
    CROSS JOIN _adg_blocker_target_salon tenant
    JOIN product_variants variant
      ON variant.salon_id = tenant.salon_id
     AND variant.sku = target.sku
     AND variant.deleted_at IS NULL
    JOIN products product
      ON product.salon_id = tenant.salon_id
     AND product.id = variant.template_id
     AND product.deleted_at IS NULL
    WHERE product.status IS DISTINCT FROM target.target_status
       OR product.is_published IS DISTINCT FROM target.target_published
       OR product.is_active IS DISTINCT FROM TRUE
  ) THEN
    RAISE EXCEPTION 'Blocker publication postcondition failed; aborting all changes';
  END IF;
END $$;

SELECT variant.sku, product.name, product.slug, product.status, product.is_active, product.is_published
FROM _adg_blocker_target_salon tenant
JOIN product_variants variant ON variant.salon_id = tenant.salon_id
JOIN _adg_blocker_targets target ON target.sku = variant.sku
JOIN products product ON product.salon_id = tenant.salon_id AND product.id = variant.template_id
ORDER BY variant.sku;

COMMIT;
`.replace(/[ \t]+\n/g, '\n');
}

function renderMarkdown(decisions, options) {
  const lines = [
    '# Asia Deli Go Catalog Blocker Unpublish Plan',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    '## Result',
    '',
    `- Products prepared for temporary unpublish: ${decisions.products.length}.`,
    '- Production mutation in this step: none.',
    '- The rows remain active/editable in admin as drafts; they are not deleted.',
    '- Search reindex is required after apply and rollback.',
    '',
    '## Products',
    '',
    '| SKU | Current product | Observed identity | Owner evidence required |',
    '| --- | --- | --- | --- |',
    ...decisions.products.map((product) => {
      const url = `${decisions.siteUrl}/products/${product.slug}`;
      return `| ${product.sku} | [${product.name}](${url}) | ${product.observedIdentity} | ${product.requiredEvidence} |`;
    }),
    '',
    '## Schema assumptions',
    '',
    '1. `product_variants.template_id = products.id` uses UUIDs, has a declared FK, and all five production samples matched.',
    '2. Every products/variants lookup is scoped by the one active `asiandeligo` channel salon; soft-deleted rows are excluded.',
    '3. Product status is varchar; `active` and `draft` are present production values. Product visibility is controlled by `products.is_published`.',
    '4. Only canonical `channels`, `products`, and `product_variants` tables are used; backup tables are ignored.',
    '',
    '## Apply and rollback',
    '',
    `- Apply SQL: \`${options.apply}\``,
    `- Rollback SQL: \`${options.rollback}\``,
    `- Owner queue: \`${options.reviewCsv}\``,
    '- Both files abort unless all five rows exactly match the expected before-state.',
    '- After either SQL file commits, call the guarded storefront `reindexSalonProducts(channel: "asiandeligo")` mutation and wait for Meilisearch.',
    '',
    '## Production approval template',
    '',
    '```text',
    'DEPLOY APPROVED: apply asiandeligo catalog blocker unpublish',
    '+ rollback: run asiandeligo-catalog-blocker-unpublish-rollback-20260713.sql, then reindex channel asiandeligo',
    '+ monitor: Zira/admin/storefront health + published count 1779 + 5 product URLs absent + storefront search',
    '```',
    '',
    '## Notes',
    '',
    ...decisions.notes.map((note) => `- ${note}`),
  ];

  return `${lines.join('\n')}\n`;
}

function renderReviewCsv(decisions) {
  const rows = [[
    'sku',
    'current_name',
    'product_url',
    'observed_identity',
    'owner_evidence_required',
    'temporary_action',
  ]];

  for (const product of decisions.products) {
    rows.push([
      product.sku,
      product.name,
      `${decisions.siteUrl}/products/${product.slug}`,
      product.observedIdentity,
      product.requiredEvidence,
      'draft_unpublished_pending_label_confirmation',
    ]);
  }

  return `${rows.map((row) => row.map(csvEscape).join(',')).join('\n')}\n`;
}

function writeFile(filePath, contents) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, contents);
}

function main() {
  const options = parseArgs();
  const decisions = JSON.parse(fs.readFileSync(options.input, 'utf8'));
  validate(decisions);
  writeFile(options.apply, renderSql(decisions, 'apply'));
  writeFile(options.rollback, renderSql(decisions, 'rollback'));
  writeFile(options.output, renderMarkdown(decisions, options));
  writeFile(options.reviewCsv, renderReviewCsv(decisions));
  console.log(`Validated ${decisions.products.length} blocker products.`);
  console.log(`Wrote ${options.output}`);
  console.log(`Wrote ${options.apply}`);
  console.log(`Wrote ${options.rollback}`);
  console.log(`Wrote ${options.reviewCsv}`);
}

main();
