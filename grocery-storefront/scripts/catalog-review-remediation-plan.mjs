#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

import { isValidGtin } from './catalog-quality-audit.mjs';

const DEFAULT_INPUT = 'docs/asiandeligo-catalog-review-decisions-20260713.json';
const DEFAULT_OUTPUT = 'docs/asiandeligo-catalog-review-remediation-20260713.md';
const DEFAULT_APPLY = 'docs/asiandeligo-catalog-review-remediation-apply-20260713.sql';
const DEFAULT_ROLLBACK = 'docs/asiandeligo-catalog-review-remediation-rollback-20260713.sql';

function parseArgs() {
  const options = {
    input: DEFAULT_INPUT,
    output: DEFAULT_OUTPUT,
    apply: DEFAULT_APPLY,
    rollback: DEFAULT_ROLLBACK,
  };

  const args = process.argv.slice(2);
  for (let index = 0; index < args.length; index += 2) {
    const flag = args[index];
    const value = args[index + 1];
    if (!['--input', '--output', '--apply', '--rollback'].includes(flag) || !value) {
      throw new Error(`Invalid argument near ${flag ?? '(end)'}`);
    }
    options[flag.slice(2)] = value;
  }
  return options;
}

function sqlLiteral(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function assertUnique(items, key, label) {
  const seen = new Set();
  for (const item of items) {
    const value = item[key];
    if (!value || seen.has(value)) throw new Error(`${label} must contain unique non-empty ${key} values`);
    seen.add(value);
  }
}

function validate(decisions) {
  if (decisions.version !== 1 || decisions.channel !== 'asiandeligo') {
    throw new Error('Unsupported decision file or channel');
  }
  if (decisions.stockDecisions?.length !== 17) throw new Error('Expected exactly 17 stock decisions');
  if (decisions.eanDecisions?.length !== 2) throw new Error('Expected exactly 2 EAN decisions');
  if (decisions.dietaryResolutions?.length !== 9) throw new Error('Expected exactly 9 dietary resolutions');

  assertUnique(decisions.stockDecisions, 'sku', 'stockDecisions');
  assertUnique(decisions.eanDecisions, 'sku', 'eanDecisions');
  assertUnique(decisions.eanDecisions, 'targetEan', 'eanDecisions');
  assertUnique(decisions.dietaryResolutions, 'sku', 'dietaryResolutions');

  for (const row of decisions.stockDecisions) {
    if (row.expectedQuantity !== 0 || row.targetQuantity !== 100) {
      throw new Error(`Unexpected stock transition for ${row.sku}`);
    }
  }
  for (const row of decisions.eanDecisions) {
    if (!isValidGtin(row.targetEan)) throw new Error(`Invalid target GTIN for ${row.sku}`);
    if (!row.expectedEan?.startsWith('mpn:')) throw new Error(`Expected legacy MPN marker for ${row.sku}`);
    if (!Array.isArray(row.sources) || row.sources.length < 2) {
      throw new Error(`EAN ${row.sku} needs at least two sources`);
    }
  }
}

function renderEanValues(rows, fromKey, toKey) {
  return rows
    .map((row) => `  (${sqlLiteral(row.sku)}, ${sqlLiteral(row[fromKey])}, ${sqlLiteral(row[toKey])})`)
    .join(',\n');
}

function renderSql(decisions, direction) {
  const isApply = direction === 'apply';
  const stockFrom = isApply ? 'expectedQuantity' : 'targetQuantity';
  const stockTo = isApply ? 'targetQuantity' : 'expectedQuantity';
  const eanFrom = isApply ? 'expectedEan' : 'targetEan';
  const eanTo = isApply ? 'targetEan' : 'expectedEan';
  const title = isApply ? 'apply' : 'rollback';

  return `-- Generated by scripts/catalog-review-remediation-plan.mjs
-- Purpose: ${title} the guarded Asia Deli Go catalog review remediation.
-- No price, product content, dietary tag, order, reservation, or publication fields are changed.
--
-- JOIN contract (schema-introspect verified 2026-07-13):
--   channels.salon_id (uuid) scopes every business-table operation.
--   product_variants.template_id (uuid) = products.id (uuid), FK declared.
--   stock_quants.variant_id (uuid) = product_variants.id (uuid), FK declared.
--   product_variants.salon_id/products.salon_id/stock_quants.salon_id all equal the channel salon_id.
--   products and product_variants exclude deleted_at rows; stock_quants has no soft-delete column.
--   trg_stock_quants_sync_variant synchronizes variant aggregate stock after quant updates.

\\set ON_ERROR_STOP on
BEGIN;

CREATE TEMP TABLE _adg_catalog_target_salon (salon_id uuid PRIMARY KEY) ON COMMIT DROP;
INSERT INTO _adg_catalog_target_salon (salon_id)
SELECT salon_id FROM channels WHERE slug = ${sqlLiteral(decisions.channel)} AND is_active = TRUE;

CREATE TEMP TABLE _adg_catalog_stock_target (
  sku text PRIMARY KEY,
  expected_quantity numeric NOT NULL,
  target_quantity numeric NOT NULL
) ON COMMIT DROP;
INSERT INTO _adg_catalog_stock_target (sku, expected_quantity, target_quantity) VALUES
${decisions.stockDecisions.map((row) => `  (${sqlLiteral(row.sku)}, ${row[stockFrom]}, ${row[stockTo]})`).join(',\n')};

CREATE TEMP TABLE _adg_catalog_ean_target (
  sku text PRIMARY KEY,
  expected_ean text NOT NULL,
  target_ean text NOT NULL UNIQUE
) ON COMMIT DROP;
INSERT INTO _adg_catalog_ean_target (sku, expected_ean, target_ean) VALUES
${renderEanValues(decisions.eanDecisions, eanFrom, eanTo)};

DO $$
DECLARE
  affected integer;
BEGIN
  IF (SELECT COUNT(*) FROM _adg_catalog_target_salon) <> 1 THEN
    RAISE EXCEPTION 'Expected exactly one active Asia Deli Go channel';
  END IF;

  IF EXISTS (
    SELECT s.sku
    FROM _adg_catalog_stock_target s
    CROSS JOIN _adg_catalog_target_salon t
    LEFT JOIN product_variants pv
      ON pv.salon_id = t.salon_id
     AND pv.sku = s.sku
     AND pv.is_active = TRUE
     AND pv.deleted_at IS NULL
    LEFT JOIN products p
      ON p.salon_id = t.salon_id
     AND p.id = pv.template_id
     AND p.is_active = TRUE
     AND p.is_published = TRUE
     AND p.deleted_at IS NULL
    LEFT JOIN stock_quants sq
      ON sq.salon_id = t.salon_id
     AND sq.variant_id = pv.id
    GROUP BY s.sku, s.expected_quantity
    HAVING COUNT(sq.id) <> 1
       OR COUNT(p.id) <> 1
       OR MIN(sq.quantity) <> s.expected_quantity
       OR MIN(sq.reserved_quantity) <> 0
  ) THEN
    RAISE EXCEPTION 'Stock precondition failed; aborting all changes';
  END IF;

  IF EXISTS (
    SELECT e.sku
    FROM _adg_catalog_ean_target e
    CROSS JOIN _adg_catalog_target_salon t
    LEFT JOIN product_variants pv
      ON pv.salon_id = t.salon_id
     AND pv.sku = e.sku
     AND pv.is_active = TRUE
     AND pv.deleted_at IS NULL
    LEFT JOIN products p
      ON p.salon_id = t.salon_id
     AND p.id = pv.template_id
     AND p.is_active = TRUE
     AND p.is_published = TRUE
     AND p.deleted_at IS NULL
    GROUP BY e.sku, e.expected_ean
    HAVING COUNT(pv.id) <> 1
       OR COUNT(p.id) <> 1
       OR MIN(pv.ean) IS DISTINCT FROM e.expected_ean
  ) THEN
    RAISE EXCEPTION 'EAN precondition failed; aborting all changes';
  END IF;

  ${isApply ? `IF EXISTS (
    SELECT 1
    FROM _adg_catalog_ean_target e
    CROSS JOIN _adg_catalog_target_salon t
    JOIN product_variants pv
      ON pv.salon_id = t.salon_id
     AND pv.ean = e.target_ean
     AND pv.deleted_at IS NULL
  ) THEN
    RAISE EXCEPTION 'A target EAN is already assigned to another variant';
  END IF;` : ''}

  UPDATE stock_quants sq
  SET quantity = s.target_quantity
  FROM _adg_catalog_target_salon t,
       _adg_catalog_stock_target s,
       product_variants pv,
       products p
  WHERE sq.salon_id = t.salon_id
    AND pv.salon_id = t.salon_id
    AND p.salon_id = t.salon_id
    AND pv.sku = s.sku
    AND pv.is_active = TRUE
    AND pv.deleted_at IS NULL
    AND p.id = pv.template_id
    AND p.is_active = TRUE
    AND p.is_published = TRUE
    AND p.deleted_at IS NULL
    AND sq.variant_id = pv.id
    AND sq.quantity = s.expected_quantity
    AND sq.reserved_quantity = 0;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.stockDecisions.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.stockDecisions.length} stock updates, got %', affected;
  END IF;

  IF EXISTS (
    SELECT s.sku
    FROM _adg_catalog_stock_target s
    CROSS JOIN _adg_catalog_target_salon t
    JOIN product_variants pv
      ON pv.salon_id = t.salon_id
     AND pv.sku = s.sku
     AND pv.is_active = TRUE
     AND pv.deleted_at IS NULL
    WHERE pv.total_stock_qty IS DISTINCT FROM s.target_quantity
       OR pv.reserved_stock_qty IS DISTINCT FROM 0
  ) THEN
    RAISE EXCEPTION 'Variant stock synchronization failed; aborting all changes';
  END IF;

  UPDATE product_variants pv
  SET ean = e.target_ean
  FROM _adg_catalog_target_salon t,
       _adg_catalog_ean_target e,
       products p
  WHERE pv.salon_id = t.salon_id
    AND pv.sku = e.sku
    AND pv.is_active = TRUE
    AND pv.deleted_at IS NULL
    AND pv.ean = e.expected_ean
    AND p.salon_id = t.salon_id
    AND p.id = pv.template_id
    AND p.is_active = TRUE
    AND p.is_published = TRUE
    AND p.deleted_at IS NULL;
  GET DIAGNOSTICS affected = ROW_COUNT;
  IF affected <> ${decisions.eanDecisions.length} THEN
    RAISE EXCEPTION 'Expected ${decisions.eanDecisions.length} EAN updates, got %', affected;
  END IF;
END $$;

SELECT pv.sku, sq.quantity, sq.reserved_quantity, pv.total_stock_qty
FROM _adg_catalog_target_salon t
JOIN product_variants pv ON pv.salon_id = t.salon_id
JOIN stock_quants sq ON sq.salon_id = t.salon_id AND sq.variant_id = pv.id
JOIN _adg_catalog_stock_target s ON s.sku = pv.sku
ORDER BY pv.sku;

SELECT pv.sku, pv.ean
FROM _adg_catalog_target_salon t
JOIN product_variants pv ON pv.salon_id = t.salon_id
JOIN _adg_catalog_ean_target e ON e.sku = pv.sku
ORDER BY pv.sku;

COMMIT;
`.replace(/[ \t]+\n/g, '\n');
}

function renderMarkdown(decisions, options) {
  const lines = [
    '# Asia Deli Go Catalog Review Remediation',
    '',
    `Generated: ${new Date().toISOString()}`,
    '',
    '## Result',
    '',
    `- Stock updates prepared: ${decisions.stockDecisions.length} (0 -> 100).`,
    `- EAN corrections prepared: ${decisions.eanDecisions.length}.`,
    `- Dietary false positives resolved in audit code: ${decisions.dietaryResolutions.length}.`,
    '- Production mutation in this step: none.',
    '',
    '## Stock',
    '',
    decisions.stockEvidence,
    '',
    '| SKU | Product | From | To |',
    '| --- | --- | ---: | ---: |',
    ...decisions.stockDecisions.map((row) => `| ${row.sku} | ${row.name} | ${row.expectedQuantity} | ${row.targetQuantity} |`),
    '',
    '## EAN',
    '',
    '| SKU | Product | Invalid value | Verified GTIN | Sources |',
    '| --- | --- | --- | --- | --- |',
    ...decisions.eanDecisions.map((row) => `| ${row.sku} | ${row.name} | ${row.expectedEan} | ${row.targetEan} | ${row.sources.map((url, index) => `[${index + 1}](${url})`).join(', ')} |`),
    '',
    '## Dietary audit fix',
    '',
    'The old substring check matched `ryb` inside `ryboflawina` and `rybonukleotyd`. The new matcher uses bounded words/stems and retains detection of actual fish/meat ingredients.',
    '',
    '| SKU | Resolution | Evidence |',
    '| --- | --- | --- |',
    ...decisions.dietaryResolutions.map((row) => `| ${row.sku} | ${row.resolution} | ${row.evidence} |`),
    '',
    '## Safety and rollback',
    '',
    '- Apply aborts unless the channel resolves once, all 17 variants are visible/active, each has exactly one zero/unreserved quant, and both EANs still have the expected legacy values.',
    '- Apply aborts if either target EAN is already in use for this salon.',
    '- Rollback aborts if stock/reservations or EANs changed after apply, preventing overwrite of real sales activity.',
    '- Apply and rollback also verify that the stock-quant trigger synchronized variant aggregate stock.',
    '- No price, dietary tag, product content, publication state, order, or reservation is changed.',
    '',
    '## Schema assumptions',
    '',
    '1. `product_variants.template_id = products.id` uses UUIDs and a declared FK; sampled matches are valid.',
    '2. `stock_quants.variant_id = product_variants.id` uses UUIDs and a declared FK; all target rows match the channel salon.',
    '3. Products/variants exclude soft-deleted rows; `stock_quants` has no soft-delete column.',
    '4. `trg_stock_quants_sync_variant` is present and synchronizes variant aggregate stock after quant changes.',
    '',
    '## Artifacts',
    '',
    `- Decisions: \`${options.input}\``,
    `- Guarded apply: \`${options.apply}\``,
    `- Guarded rollback: \`${options.rollback}\``,
  ];
  return `${lines.join('\n')}\n`;
}

function writeFile(filePath, contents) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, contents);
}

function main() {
  const options = parseArgs();
  const decisions = JSON.parse(fs.readFileSync(options.input, 'utf8'));
  validate(decisions);
  writeFile(options.apply, renderSql(decisions, 'apply'));
  writeFile(options.rollback, renderSql(decisions, 'rollback'));
  writeFile(options.output, renderMarkdown(decisions, options));
  console.log(`Validated ${decisions.stockDecisions.length} stock, ${decisions.eanDecisions.length} EAN, and ${decisions.dietaryResolutions.length} dietary decisions.`);
  console.log(`Wrote ${options.output}`);
  console.log(`Wrote ${options.apply}`);
  console.log(`Wrote ${options.rollback}`);
}

main();
